package com.edc.edcportal.core.lovapi.pojo;

public class LovItem {
    private String lovValue;
    private String lovName;
    private String lovEnName;
    private String lovId;

    public String getLovValue() {
        return lovValue;
    }

    public void setLovValue(String lovValue) {
        this.lovValue = lovValue;
    }

    public String getLovName() {
        return lovName;
    }

    public void setLovName(String lovName) {
        this.lovName = lovName;
    }

    public String getLovEnName() {
        return lovEnName;
    }

    public void setLovEnName(String lovEnName) {
        this.lovEnName = lovEnName;
    }

    public String getLovId() {
        return lovId;
    }

    public void setLovId(String lovId) {
        this.lovId = lovId;
    }

}
