/**
 * @author ACN
 * @version 1.0.0
 * @since 1.0.0
 * 
 * 
 * Backing Java code data for GRC Search result.
 * 
 */
public class GRCRegistry
{
	private String tag; 
	private String busRegName;
	private String busRegDesc;
	private String busRegIcon;
	private String busRegUrl;
	private String busRegUrlText;
	private String busRegTipsTitle;
	private String busRegTips;
	private String code;
	private String name;

	public String getTag() {
		return tag;
	}

	public String getBusRegName() {
		return busRegName;
	}

	public String getBusRegDesc() {
		return busRegDesc;
	}

	public String getBusRegIcon() {
		return busRegIcon;
	}

	public String getBusRegUrl() {
		return busRegUrl;
	}

	public String getBusRegUrlText() {
		return busRegUrlText;
	}

	public String getBusRegTips() {
		return busRegTips;
	}

	public String getBusRegTipsTitle() {
		return busRegTipsTitle;
	}

	public String getCode() {
		return code;
	}

	public String getName() {
		return name;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public void setBusRegName(String busRegName) {
		this.busRegName = busRegName;
	}

	public void setBusRegDesc(String busRegDesc) {
		this.busRegDesc = busRegDesc;
	}

	public void setBusRegIcon(String busRegIcon) {
		this.busRegIcon = busRegIcon;
	}

	public void setBusRegUrl(String busRegUrl) {
		this.busRegUrl = busRegUrl;
	}

	public void setBusRegUrlText(String busRegUrlText) {
		this.busRegUrlText = busRegUrlText;
	}

	public void setBusRegTips(String busRegTips) {
		this.busRegTips = busRegTips;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setBusRegTipsTitle(String busRegTipsTitle) {
		this.busRegTipsTitle = busRegTipsTitle;
	}
}
