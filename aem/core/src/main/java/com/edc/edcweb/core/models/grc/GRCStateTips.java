package com.edc.edcweb.core.models.grc;

public class GRCStateTips {

	private String stateName;
	private String stateTips;
	private String stateTipsTitle;
	
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public String getStateTips() {
		return stateTips;
	}
	public void setStateTips(String stateTips) {
		this.stateTips = stateTips;
	}

	public String getStateTipsTitle() {
		return stateTipsTitle;
	}
	public void setStateTipsTitle(String stateTipsTitle) {
		this.stateTipsTitle = stateTipsTitle;
	}
}
