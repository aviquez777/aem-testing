package com.edc.edcweb.core.lovapi.pojo;

public class BankItem {
    private String bankValue;
    private String bankName;
    private String bankDomain;

    public String getBankValue() {
        return bankValue;
    }

    public void setBankValue(String bankValue) {
        this.bankValue = bankValue;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public String getBankDomain() {
        return bankDomain;
    }

    public void setBankDomain(String bankDomain) {
        this.bankDomain = bankDomain;
    }

}
